%IMPZ  デジタルフィルタのインパルス応答
%
%   [H,T] = IMPZ(B,A) は、B/A のフィルタのインパルス応答を列ベクトル H に、
%   時間を T (または、サンプル間隔 T = [0 1 2 ...]') に返します。
%
%   [H,T] = IMPZ(B,A,N) は、N 個のサンプルのインパルス応答を計算します。
%   N が整数ベクトルの場合、IMPZ は整数で表される位置 (原点は 0) での
%   インパルス応答を計算します。
%
%   [H,T] = IMPZ(B,A,N,Fs) は、サンプルの単位間隔が 1/Fs になるように 
%   N 個のサンプルスケール T を計算します。Fsのデフォルトは 1 です。
%
%   [H,T] = IMPZ(B,A,[],Fs) は、サンプル数を選択し、サンプルの単位間隔が 
%   1/Fs 単位の間隔となるように T を換算します。
%
%   出力引数を設定せずに IMPZ を使用すると、STEM(T,H) を使って現在の 
%   Figure ウィンドウ内にインパルス応答をプロットします。
%
%   参考 IMPULSE (連続システムに対する Controls Toolbox の関数)


